var LAYER_COUNT = level.layers.length;
var MAP = {
	tw : level.width,
	th : level.height
};

var TILE = level.tilewidth;
var TILESET_TILE = 	  level.tilesets[0].tilewidth;
var TILESET_SPACING = level.tilesets[0].spacing;
var TILESET_PADDING = level.tilesets[0].margin;

var TILESET_COUNT_X = 14;
var TILESET_COUNT_Y = 14;

//collision enum
var LAYER_COUNT = 3;
var LAYER_BACKGROUND = 0;
var LAYER_PLATFORMS = 1;
var LAYER_LADDERS = 2;

var tileset = document.createElement("img");
tileset.src = level.tilesets[0].image;

function drawMap(cam_x, cam_y)
{
	//loop through all our layers
	for (var layer = 0; layer < LAYER_COUNT; layer++)
	{
		var idx = 0;
		//loop vertically all of out tiles
		for (var y = 0; y < level.layers[layer].height; y++)
		{
			//loop through horizontally all of out tileset
			for (var x = 0; x < level.layers[layer].width; x++)
			{
				if (level.layers[layer].data[idx] != 0)
				{
					var tileIndex = level.layers[layer].data[idx] - 1;
					var sx = TILESET_PADDING + (tileIndex % TILESET_COUNT_X) * (TILESET_TILE + TILESET_SPACING);
					var sy = TILESET_PADDING + ( Math.floor(tileIndex / TILESET_COUNT_Y) * (TILESET_TILE + TILESET_SPACING));

					context.drawImage(tileset, sx, sy, TILESET_TILE, TILESET_TILE, x * TILE - cam_x, (y - 1) * TILE - cam_y, TILESET_TILE, TILESET_TILE);
				}
				idx++;
			}
		}
	}	
}

function cellAtPixelCoord(layer, x, y)
{
	//if (x < 0 || x > SCREEN_WIDTH || y < 0)
	//	return 1;
    //
	//if (y > SCREEN_HEIGHT)
	//	return 0;
	
	return cellAtTileCoord(layer, pixelToTile(x), pixelToTile(y));
}

function cellAtTileCoord(layer, tx, ty)
{
	if (tx < 0 || tx >= MAP.tw || ty < 0)
		return 1;
	if (ty >= MAP.th)
		return 0;
	
	return cells[layer][ty][tx];
}

function tileToPixel(tile)
{
	return tile * TILE;
}

function pixelToTile(pixel)
{
	return Math.floor(pixel/TILE);
}

function bound(value, min, max)
{
	if (value < min)
		return min;
	if (value > max)
		return max;
	return value;
}


